import math

# 파이와 자연상수
pi_value = math.pi
e_value = math.e

# 절대값
abs_value = math.fabs(-10.5)

# 올림
ceil_value = math.ceil(3.4)

# 내림
floor_value = math.floor(3.7)

# 반올림
round_value =round(3.5)

# 제곱근
sqrt_value = math.sqrt(16)

# 지수 함수
exp_value = math.exp(1)  # e^1

# 로그 함수
log_value = math.log(2.7183)  # 자연 로그, e를 밑으로 함
